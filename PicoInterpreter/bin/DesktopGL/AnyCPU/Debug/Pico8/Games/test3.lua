--[[
  create a shape object with a table of triangles
  stores on the objects variable

  shape = shape defined at the beginning at the code
  pos = position to create the object
--]]
function create_object(type, pos)
  local obj = {}
  obj.pos = pos
  obj.type = type


  obj.type.init(obj)
  add(objects, obj)
  return obj
end

